        Overtime overtime= new Overtime();
        Date OvertimeDate = overtimeDate.getDate();
        String overtimeStatus = "Pending";
        int overtimeHours = Integer.parseInt(overtimehour.getSelectedItem().toString());

        // Check for invalid dates
        if (!isValidLeaveDates(OvertimeDate)) {
            JOptionPane.showMessageDialog(null, "Invalid overtime dates. Please ensure the overtime date is after today.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method if dates are invalid
        }

        try {
            overtime.setOvertimeDetails(EmployeeID, OvertimeDate, overtimeStatus, overtimeHours);
            overtime.recordOvertime(); // Call recordLeave method after setting details
            // Display success message (optional)
            JOptionPane.showMessageDialog(null, "Overtime application submitted!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException | ClassNotFoundException | ParseException ex) {
            // Handle database errors and ParseException (optional)
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error submitting overtime application: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        jButton1.setEnabled(false);
        
        Timer timer = new Timer(180000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent evt) {
                jButton1.setEnabled(true);
            }
        });

        // Set the timer to execute only once
        timer.setRepeats(false);

        // Start the timer
        timer.start();
